    @push('scripts')
        <script>
            function copyValue(credentialId) {
                fetch(`/credentials/${credentialId}/copy`, {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                            'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content
                        }
                    })
                    .then(response => response.json())
                    .then(data => {
                        navigator.clipboard.writeText(data.value).then(() => {
                            alert('Credential value copied to clipboard!');
                        });
                    })
                    .catch(error => {
                        console.error('Error:', error);
                        alert('Failed to copy credential value');
                    });
            }
        </script>
